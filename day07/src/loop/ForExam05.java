package loop;


public class ForExam05 {

	public static void main(String[] args) {
		for(int i = 1; i <= 5; i++) {
			System.out.println("i : " + i);
			for(int j = 1; j <= 5; j++) {
				System.out.print("j : " + j + " ");
			}
			System.out.println();
			System.out.println("---------------");
		}
		
		}

}
