package operator1;

public class OperatorExam5 {

	public static void main(String[] args) {
		int a = 3;
		
//		 a = a + 7;
//		 System.out.println("a = a + 7 => " + a);
		a += 7;
		System.out.println("a += 7 => " + a); //10
		
		a -= 2; // a = a - 2
		System.out.println("a -= 2 => " + a); //8
		
		a *= 2;
		System.out.println("a *= 2 => " + a); //16
		
		a /= 2;
		System.out.println("a /= 2 => " + a); //8
		
	}

}
